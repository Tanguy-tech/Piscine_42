#include <unistd.h>

void	ft_print_tab(char *str, int size)
{
	int i;

	i = 0;
	while (str[i] && (str[i] > 47 && str[i] < 58))
	{
		write(1, &str[i], 1);
		i++;
		if (i % size == 0)
			write(1, "\n", 1);
		else
			write(1, " ", 1);
	}
}
