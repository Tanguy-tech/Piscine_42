#include <stdlib.h>

int		*ft_range(int min, int max)
{
	int *tab;
	int i;
	int range;

	range = max - min;
	if (!(tab = (int *)malloc(sizeof(int) * range)))
		return (0);
	if (min >= max)
		return (0);
	i = 0;
	while (min < max)
	{
		tab[i] = min;
		i++;
		min++;
	}
	return (tab);
}
