#include "bsq.h"

int		ft_atoi(char *str)
{
	int i;
	int res;

	i = 0;
	res = 0;
	while (str[i])
	{
		while (str[i] >= '0' && str[i] <= '9')
			res = res * 10 + (str[i++] - 48);
		return (res);
	}
	return (0);
}
